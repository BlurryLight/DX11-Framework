/*!
\brief
  Constructor, calls DX11 to create the IDXGIFactory1
*/
/****************************************************************************/
DX11::Factory::Factory()
{
    if (!SUCCEEDED(CreateDXGIFactory1(__uuidof(IDXGIFactory1), reinterpret_cast<void**>(ReleaseAndGetAddressOf()))))
    {
        throw std::runtime_error("DX11: CreateDXGIFactory1() failed!\n");
    }
}